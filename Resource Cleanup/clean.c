static int running = 1;

void handle_sigint(int sig) {
    (void)sig;
    running = 0;
}

static int run_supervisor(const char *rootfs)
{
    supervisor_ctx_t ctx;
    int rc;

    memset(&ctx, 0, sizeof(ctx));

    rc = pthread_mutex_init(&ctx.metadata_lock, NULL);
    if (rc != 0) {
        errno = rc;
        perror("pthread_mutex_init");
        return 1;
    }

    rc = bounded_buffer_init(&ctx.log_buffer);
    
    if (rc != 0) {
        errno = rc;
        perror("bounded_buffer_init");
        pthread_mutex_destroy(&ctx.metadata_lock);
        return 1;
    }
    
    ctx.should_stop = 0;

    if (pthread_create(&ctx.logger_thread, NULL, logging_thread, &ctx) != 0) {
	perror("pthread_create");
	return 1;
    }

    printf("Supervisor started with base rootfs: %s\n", rootfs);

    signal(SIGINT, handle_sigint);
    signal(SIGCHLD, SIG_IGN);

    child_config_t config;
    memset(&config, 0, sizeof(config));

    strcpy(config.id, "c1");
    strcpy(config.rootfs, rootfs);
    strcpy(config.command, "/bin/sh");

    void *stack = malloc(STACK_SIZE);
    if (!stack) {
        perror("malloc");
        return 1;
    }

    pid_t pid = clone(child_fn, stack + STACK_SIZE,
                      CLONE_NEWPID | CLONE_NEWUTS | CLONE_NEWNS | SIGCHLD,
                      &config);

    if (pid < 0) {
        perror("clone");
        return 1;
    }

    printf("Container started with PID %d\n", pid);
    printf("TASK1 : Container created successfully\n");

    while (running) {
        sleep(1);
    }
    
    
    printf("TASK1: Sending SIGKILL to container\n");
    printf("Stopping container...\n");
    kill(pid, SIGKILL);
    waitpid(pid, NULL, 0);
    printf("TASK1: Child process reaped\n"); 
    
    ctx.should_stop = 1;
    pthread_join(ctx.logger_thread, NULL);

    free(stack);

    bounded_buffer_begin_shutdown(&ctx.log_buffer);
    bounded_buffer_destroy(&ctx.log_buffer);
    pthread_mutex_destroy(&ctx.metadata_lock);

    printf("Cleanup done\n");
    return 0;
}

