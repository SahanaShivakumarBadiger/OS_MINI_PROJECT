void *logging_thread(void *arg)
{
    supervisor_ctx_t *ctx = (supervisor_ctx_t *)arg;

    printf("LOG_THREAD: started\n");

    while (!ctx->should_stop) {
        sleep(1);
        printf("LOG_THREAD: running...\n");
    }

    printf("LOG_THREAD: exiting\n");
    return NULL;
}
